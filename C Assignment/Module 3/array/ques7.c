//Find out length of string without using inbuilt function
#include<stdio.h>
int main()
{
    char str[20];
    int i,len;
    printf("Enter String: ");
    scanf("%d",&str[20]);
    for(i=0;i<20;i++)
    {
        len=str[i];
        
    }
    len++;
    printf("Length Of String Is: %d",len);

}


